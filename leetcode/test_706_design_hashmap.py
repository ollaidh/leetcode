"""
Design a HashMap without using any built-in hash table libraries.

Implement the MyHashMap class:

MyHashMap() initializes the object with an empty map.
void put(int key, int value) inserts a (key, value) pair into the HashMap.
If the key already exists in the map, update the corresponding value.
int get(int key) returns the value to which the specified key is mapped,
or -1 if this map contains no mapping for the key.
void remove(key) removes the key and its corresponding value
if the map contains the mapping for the key.
"""

import unittest


class MyHashMap:
    """
    Collision resolution: list for colliding key-value pairs
    We store all key-values pairs in a list for each bucket.
    When a collision occurs we just add another (key, value) tuple to the
    corresponding bucket list. When we want to get a value by key we just
    go to the corresponding bucket and search through the list to find
    a key for which we want to get a value. Voila!
    The main disadvantage of this collision resolution method is that
    we use Python list, and deleting elements is O(N) complexity.
    This might be addressed by using linked list instead.
    """
    def __init__(self):
        self.buckets: list[list[tuple[int, int]] | None] = [None] * 42
        self.elements = 0
        self.full_threshold = 0.3
        self.is_rehashing = False

    def rehash(self):
        assert not self.is_rehashing
        self.is_rehashing = True
        buckets = self.buckets
        self.buckets = [None] * (2 * len(self.buckets) + 1)
        self.elements = 0
        for bucket in buckets:
            if bucket:
                for elem in bucket:
                    if elem:
                        self.put(elem[0], elem[1])
        self.is_rehashing = False

    def put(self, key: int, value: int) -> None:
        ind = key % len(self.buckets)

        if self.buckets[ind]:
            for i in range(len(self.buckets[ind])):
                if self.buckets[ind][i][0] == key:
                    self.buckets[ind][i] = (key, value)
                    return
            self.buckets[ind].append((key, value))
        else:
            self.buckets[ind] = [(key, value)]
        self.elements += 1

        if self.elements / len(self.buckets) > self.full_threshold:
            self.rehash()

    def get(self, key: int) -> int:
        ind = key % len(self.buckets)
        if not self.buckets[ind]:
            return -1
        for i in range(len(self.buckets[ind])):
            if self.buckets[ind][i][0] == key:
                return self.buckets[ind][i][1]
        return -1

    def remove(self, key: int) -> None:
        ind = key % len(self.buckets)
        if not self.buckets[ind]:
            return
        for i in range(len(self.buckets[ind])):
            if self.buckets[ind][i][0] == key:
                del self.buckets[ind][i]
                self.elements -= 1
                return


class MyFlatHashMap:
    """
    Collision resolution: open addressing
    When a collision occurs we continue to search for an empty
    bucket starting from collided bucket with an increment 1.
    When first empty bucket found, put (key, value) tuple there.
    We can use other increment but for simplicity we use 1 here.

    There is a following problem: when certain key is deleted,
    it's bucket stores None value. If a collision occurred before
    deletion, we will never find another (key, value) pair that
    caused this collision and was put into other bucket,
    as we see None value in th bucket and return -1.

    So special value (-1) for deleted buckets was inserted into
    the map instead of None. Now if we are trying to get (key, value)
    pair and see -1 in the bucket where it should be, we continue
    searching through the buckets, and if this key exists we will
    eventually find it.
    """
    def __init__(self):
        self.buckets: list[tuple[int, int] | None] = [None] * 42
        self.elements = 0
        self.full_threshold = 0.3
        self.is_rehashing = False

    def rehash(self):
        assert not self.is_rehashing
        self.is_rehashing = True
        buckets = self.buckets
        self.buckets = [None] * (2 * len(self.buckets) + 1)
        self.elements = 0
        for b in buckets:
            if b and b[0] >= 0:
                self.put(b[0], b[1])
        self.is_rehashing = False

    def put(self, key: int, value: int) -> None:
        ind = key % len(self.buckets)

        while self.buckets[ind]:
            if self.buckets[ind][0] == key or self.buckets[ind][0] == -1:
                self.buckets[ind] = (key, value)
                return
            else:
                ind = (ind + 1) % len(self.buckets)

        self.buckets[ind] = (key, value)
        self.elements += 1

        if self.elements / len(self.buckets) > self.full_threshold:
            self.rehash()

    def get(self, key: int) -> int:
        ind = key % len(self.buckets)
        while True:
            if not self.buckets[ind]:
                return -1
            if self.buckets[ind][0] == key:
                return self.buckets[ind][1]
            ind = (ind + 1) % len(self.buckets)

    def remove(self, key: int) -> None:
        ind = key % len(self.buckets)
        while True:
            if not self.buckets[ind]:
                return
            if self.buckets[ind][0] == key:
                self.buckets[ind] = (-1, 0)
                self.elements -= 1
                return
            ind = (ind + 1) % len(self.buckets)


class TestMyHashMap(unittest.TestCase):
    def test_hashmap(self):
        maps = [MyHashMap(), MyFlatHashMap()]
        for hash_map in maps:
            hash_map.put(1, 11)
            hash_map.put(2, 22)
            hash_map.put(3, 33)
            hash_map.put(1, 12)
            hash_map.put(4, 44)
            self.assertEqual(hash_map.elements, 4)
            self.assertEqual(hash_map.get(3), 33)
            self.assertEqual(hash_map.get(1), 12)

            hash_map.remove(4)
            self.assertEqual(hash_map.elements, 3)
            self.assertEqual(hash_map.get(4), -1)

            hash_map.remove(4)
            self.assertEqual(hash_map.elements, 3)
            self.assertEqual(hash_map.get(4), -1)

            hash_map.remove(999)
            self.assertEqual(hash_map.elements, 3)

    def test_rehash(self):
        maps = [MyHashMap(), MyFlatHashMap()]
        for hash_map in maps:
            for i in range(1000):
                hash_map.put(i, i)
                self.assertEqual(hash_map.get(i), i)

